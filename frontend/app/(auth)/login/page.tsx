'use client';

import { useState } from 'react';
import Link from 'next/link';
import { useRouter } from 'next/navigation';
import { Input, TreeAnimation } from '@/components/ui';
import { auth } from '@/lib/auth';

/**
 * Login Page with Tree Animation
 * Matches the Eco-Luxury design from frontend-base/
 */

export default function LoginPage() {
  const router = useRouter();
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState('');

  const handleLogin = async (e: React.FormEvent) => {
    e.preventDefault();
    setIsLoading(true);
    setError('');
    
    console.log('=== LOGIN DEBUG ===');
    console.log('Email:', email);
    console.log('Password length:', password.length);
    
    try {
      console.log('>>> Calling auth.login...');
      const result = await auth.login({ email, password });
      console.log('>>> Login result:', result);
      console.log('>>> Token saved:', localStorage.getItem('nature_access_token')?.substring(0, 20) + '...');
      router.push('/');
    } catch (err) {
      console.error('>>> Login error:', err);
      setError('Invalid email or password. Please try again.');
    } finally {
      setIsLoading(false);
    }
  };

  const handleGoogleLogin = () => {
    // Redirect to Django backend Google OAuth
    window.location.href = auth.getGoogleAuthUrl();
  };

  return (
    <div className="min-h-screen w-full flex items-center justify-center font-display bg-[#0B1610] relative overflow-hidden">
      {/* Background with blur */}
      <div
        className="absolute inset-0 w-full h-full bg-center bg-cover opacity-40 blur-sm scale-110"
        style={{
          backgroundImage:
            'url("https://lh3.googleusercontent.com/aida-public/AB6AXuDGJ2EHp_b9TsnrJTCV-qAj3gRsfWcFBcK2LhUvsPOEk6MDZ8r1e36sq06GCmQdVMYzctaJCAFd-v4bSuQsYsPmu_d6NytOSM7G9-3R7YPg5KAd6Wk17ZunBDPlY9bzK6aFMBvTP8-T_ERvhOhD1LzsrO5SPV9qKSvw048exJzTj1PLOv3kc6BYkrKHIunH1YIjYvn0AhgVOwdjovX19uOlJFX8OJ4WUg0xD-W4g0EhqKebAozk1VI2bELxwkgz1NuYULihB6jfr01w")',
        }}
      />

      <div className="relative z-10 w-full max-w-md px-6">
        {/* The Tree Container */}
        <div className="bg-background-dark/80 backdrop-blur-xl border border-wood-light/30 rounded-2xl p-8 shadow-2xl relative">
          {/* Back Button */}
          <Link
            href="/"
            className="absolute top-4 left-4 text-white/60 hover:text-white"
          >
            <span className="material-symbols-outlined">arrow_back</span>
          </Link>

          {/* Tree Animation */}
          <TreeAnimation passwordLength={password.length} />

          <div className="text-center mb-6">
            <h2 className="text-2xl font-bold text-white mb-1">
              Grow Your Account
            </h2>
            <p className="text-white/60 text-sm">
              Enter your details to nurture your profile.
            </p>
          </div>

          <form onSubmit={(e) => { console.log('>>> FORM SUBMIT'); handleLogin(e); }} className="space-y-4">
            {error && (
              <div className="bg-red-500/10 border border-red-500/30 text-red-400 text-sm p-3 rounded-lg">
                {error}
              </div>
            )}
            
            <Input
              label="Plant your Email"
              icon="seed"
              type="email"
              value={email}
              onChange={(e) => setEmail(e.target.value)}
              placeholder="you@nature.com"
              required
            />

            <Input
              label="Water with Password"
              icon="water_drop"
              type="password"
              value={password}
              onChange={(e) => setPassword(e.target.value)}
              placeholder="••••••••"
              required
            />

            <button
              type="submit"
              disabled={isLoading}
              className="w-full bg-gradient-to-r from-olive-green to-primary text-background-dark font-bold text-lg py-4 rounded-xl mt-6 hover:brightness-110 transition-all shadow-lg shadow-primary/20 flex items-center justify-center gap-2 group disabled:opacity-50"
            >
              {isLoading ? (
                <span className="material-symbols-outlined animate-spin">
                  progress_activity
                </span>
              ) : (
                <>
                  <span>Start Growing</span>
                  <span className="material-symbols-outlined group-hover:-translate-y-1 transition-transform">
                    forest
                  </span>
                </>
              )}
            </button>
          </form>

          {/* Divider */}
          <div className="relative flex items-center py-6">
            <div className="flex-grow border-t border-white/10" />
            <span className="flex-shrink mx-4 text-white/40 text-sm">OR</span>
            <div className="flex-grow border-t border-white/10" />
          </div>

          {/* Google Login Button */}
          <button
            onClick={handleGoogleLogin}
            className="w-full bg-white text-background-dark font-bold text-base py-3 rounded-xl hover:bg-white/90 transition-all flex items-center justify-center gap-3"
          >
            <svg className="w-6 h-6" viewBox="0 0 24 24">
              <path
                fill="#4285F4"
                d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
              />
              <path
                fill="#34A853"
                d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
              />
              <path
                fill="#FBBC05"
                d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
              />
              <path
                fill="#EA4335"
                d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
              />
            </svg>
            <span>Continue with Google</span>
          </button>

          <p className="text-center text-white/40 text-sm mt-6">
            Don&apos;t have a seed yet?{' '}
            <Link
              href="/register"
              className="text-primary cursor-pointer hover:underline"
            >
              Get one here
            </Link>
          </p>
        </div>
      </div>
    </div>
  );
}
